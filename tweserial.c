/**
 * fputc を用いた fputs 実装
 *
 * @param s   出力文字列
 * @param fp  出力ストリーム
 * @return 0
 */
int _TWE_fputs(const char *s, TWE_tsFILE *fp) {
	while (*s != '\0') {
		fp->fp_putc(*s++, fp);
	}
	return 0;
}